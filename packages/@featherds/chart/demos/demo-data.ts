export const generateNetworkData = (size: number) => {
  const numberOfNodes = size;
  const nodes = [];
  const links = [];

  // Generate nodes
  for (let i = 1; i <= numberOfNodes; i++) {
    nodes.push({
      id: `node${i}`,
      group: (i % 10) + 1, // Example group logic: group numbers from 1 to 10
      // group: 5, // Example group logic: group numbers from 1 to 10
    });
  }

  // Generate links
  for (let i = 1; i <= numberOfNodes; i++) {
    // Simple strategy to connect each node with the next one and one more, skipping some for variety
    const target1 = (i % numberOfNodes) + 1;
    const target2 = ((i + 6) % numberOfNodes) + 1;
    // links.push({ source: `node${i}`, target: `node${target1}`, value: 1 });
    if (i % 3 === 0) {
      // Every fifth node connects to two others for variety
      links.push({ source: `node${i}`, target: `node${target2}`, value: 1 });
    }
    if (i % 4 === 0) {
      // Every fifth node connects to two others for variety
      links.push({ source: `node${i}`, target: `node${target2}`, value: 1 });
      links.push({
        source: `node${i - 1}`,
        target: `node${target1}`,
        value: 1,
      });
    }
  }

  return { data: { nodes, links } };
};

